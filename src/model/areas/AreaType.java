package model.areas;

/**
 * Types of areas in the warehouse.
 */
public enum AreaType {

    /**
     * Representation of path between shelves.
     */
    PATH,
    /**
     * Representation of unloading area in the warehouse.
     */
    UNLOADING,
    /**
     * Representation of parking area for carts.
     */
    PARKING,
    /**
     * Representation of area occupied by shelf.
     */
    SHELVING

}
